<h5><b> KELILING DANAU TOBA, Kaldera Terbesar di Dunia </b></h5>
<p>Hallo teman-teman MyShare Olala,
Vlog kali ini saya mengajak kamu Keliling Danau Toba yang terkenal itu. Yang kita kelilingi kali ini adalah Danau Toba sisi Parapat, Kab. Simalungun, Sumatera Utara. Wilayah yang merupakan kaldera terbesar di dunia itu merupakan peninggalan aktivitas supervolcano yang terjadi ribuan tahun lalu, dan meninggalkan kontur alam yang sangat indah dan memanjakan mata melihatnya. Danau dengan air yang berwarna biru sejauh mata memandang mengisi lembah-lembah yang sebetulnya adalah bagian kawah yang maha luas. Bukit-bukit tandus yang mengelilingi danau juga menunjukkan siluet dan relief alam yang mempesona, disamping tentu saja Pulau Samosir yang berada ditengah-tengah danau toba ini.

Aktivitas supervolcano ini sebetulnya masih aktif namun tak ada siapapun ilmuwan yang dapat memperkirakan kapan aktivitas supervolcano dapat terjadi seperti ribuan tahun lalu di wilayah ini.

